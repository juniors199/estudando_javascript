let nomes = ["Marcio", "Joao", "Pedro"];

//Neste caso ele atribui uma variavel ao indice, podendo trazer o valor chamando a variavel nova.
// Sintaxe: let [variavel1, variavel2(Relativo ao indice)] = array.
let [nomeA, nomeB, nomeC] = nomes;

console.log(nomeA);
console.log(nomeB);
console.log(nomeC);

